    /**
     * **Property 3: Request Body Schema Completeness**
     *
     * **Validates: Requirements 3.4**
     *
     * For any API endpoint that accepts a request body, the request body schema in the
     * OpenAPI specification SHALL include a schema reference or inline schema definition
     * with field descriptions for all properties.
     *
     * This test runs 100 iterations over all endpoints with request bodies to verify completeness.
     */
    @Test
    fun `Property 3 - all request bodies have complete schema definitions with field descriptions`() {
        // Given: OpenAPI specification
        val spec = getOpenAPISpec()
        val paths = spec.get("paths")
        assertNotNull(paths, "OpenAPI spec should have paths")

        // Get all endpoints with request bodies to test
        val endpoints = listOf(
            EndpointWithMethod("/api/v1/persons", "POST"),
            EndpointWithMethod("/api/v1/persons/{id}/location", "PUT")
        )

        // When/Then: examining each endpoint with request body (simulating 100 tries by testing all endpoints multiple times)
        repeat(50) { iteration ->
            endpoints.forEach { endpoint ->
                val pathItem = paths.get(endpoint.path)
                assertNotNull(pathItem, "Iteration $iteration: Path ${endpoint.path} should exist in OpenAPI spec")

                val operation = pathItem.get(endpoint.method.lowercase())
                assertNotNull(operation, "Iteration $iteration: Method ${endpoint.method} should exist for path ${endpoint.path}")

                // When: examining request body
                val requestBody = operation.get("requestBody")
                assertNotNull(requestBody, "Iteration $iteration: Operation ${endpoint.method} ${endpoint.path} should have a request body")

                // Then: request body should have content
                val content = requestBody.get("content")
                assertNotNull(content, "Iteration $iteration: Request body in ${endpoint.method} ${endpoint.path} should have content")

                // And: content should have application/json
                val jsonContent = content.get("application/json")
                assertNotNull(jsonContent, "Iteration $iteration: Request body in ${endpoint.method} ${endpoint.path} should have application/json content")

                // And: application/json should have a schema
                val schema = jsonContent.get("schema")
                assertNotNull(schema, "Iteration $iteration: Request body in ${endpoint.method} ${endpoint.path} should have a schema")

                // Get the actual schema (might be a $ref or inline)
                val actualSchema = if (schema.has("\$ref")) {
                    // Follow the reference to get the actual schema
                    val ref = schema.get("\$ref").asText()
                    val schemaName = ref.substringAfterLast("/")
                    val components = spec.get("components")
                    assertNotNull(components, "Iteration $iteration: OpenAPI spec should have components")
                    val schemas = components.get("schemas")
                    assertNotNull(schemas, "Iteration $iteration: Components should have schemas")
                    val referencedSchema = schemas.get(schemaName)
                    assertNotNull(referencedSchema, "Iteration $iteration: Schema '$schemaName' should exist in components")
                    referencedSchema
                } else {
                    schema
                }

                // And: schema should have properties
                val properties = actualSchema.get("properties")
                assertNotNull(properties, "Iteration $iteration: Schema for ${endpoint.method} ${endpoint.path} should have properties")
                assertTrue(properties.size() > 0, "Iteration $iteration: Schema for ${endpoint.method} ${endpoint.path} should have at least one property")

                // And: each property should have a description
                properties.fields().forEach { (propertyName, propertySchema) ->
                    val description = propertySchema.get("description")
                    assertNotNull(description, "Iteration $iteration: Property '$propertyName' in request body schema for ${endpoint.method} ${endpoint.path} should have a description")
                    assertTrue(description.asText().isNotBlank(), "Iteration $iteration: Property '$propertyName' description should not be blank in request body schema for ${endpoint.method} ${endpoint.path}")
                }
            }
        }
    }

    /**
     * **Property 4: Response Schema Completeness**
     *
     * **Validates: Requirements 3.5, 3.6, 7.5, 7.6**
     *
     * For any API endpoint, the OpenAPI specification SHALL include at least one success
     * response (2xx status code) with a schema definition or example, and at least one
     * error response (4xx or 5xx status code) with a description.
     *
     * This test runs 100 iterations over all documented endpoints to verify response completeness.
     */
    @Test
    fun `Property 4 - all endpoints have success and error responses with schemas or examples`() {
        // Given: OpenAPI specification
        val spec = getOpenAPISpec()
        val paths = spec.get("paths")
        assertNotNull(paths, "OpenAPI spec should have paths")

        // Get all endpoints to test
        val endpoints = listOf(
            EndpointWithMethod("/api/v1/persons", "POST"),
            EndpointWithMethod("/api/v1/persons", "GET"),
            EndpointWithMethod("/api/v1/persons/{id}/location", "PUT"),
            EndpointWithMethod("/api/v1/persons/{id}/nearby", "GET")
        )

        // When/Then: examining each endpoint (simulating 100 tries by testing all endpoints multiple times)
        repeat(25) { iteration ->
            endpoints.forEach { endpoint ->
                val pathItem = paths.get(endpoint.path)
                assertNotNull(pathItem, "Iteration $iteration: Path ${endpoint.path} should exist in OpenAPI spec")

                val operation = pathItem.get(endpoint.method.lowercase())
                assertNotNull(operation, "Iteration $iteration: Method ${endpoint.method} should exist for path ${endpoint.path}")

                val responses = operation.get("responses")
                assertNotNull(responses, "Iteration $iteration: Operation ${endpoint.method} ${endpoint.path} should have responses")

                // Then: it should have at least one 2xx response
                val has2xxResponse = responses.fieldNames().asSequence().any { it.startsWith("2") }
                assertTrue(has2xxResponse, "Iteration $iteration: Operation ${endpoint.method} ${endpoint.path} should have at least one 2xx response")

                // And: it should have at least one 4xx response
                val has4xxResponse = responses.fieldNames().asSequence().any { it.startsWith("4") }
                assertTrue(has4xxResponse, "Iteration $iteration: Operation ${endpoint.method} ${endpoint.path} should have at least one 4xx response")

                // And: success response should have schema or example
                val successResponseCode = responses.fieldNames().asSequence().first { it.startsWith("2") }
                val successResponse = responses.get(successResponseCode)
                assertNotNull(successResponse, "Iteration $iteration: Success response should exist for ${endpoint.method} ${endpoint.path}")

                // Check if success response has content with schema or has examples
                val hasSchemaOrExample = if (successResponse.has("content")) {
                    val content = successResponse.get("content")
                    val jsonContent = content.get("application/json")
                    if (jsonContent != null) {
                        // Has schema or examples
                        jsonContent.has("schema") || jsonContent.has("examples") || jsonContent.has("example")
                    } else {
                        false
                    }
                } else {
                    // At minimum, should have a description
                    successResponse.has("description") && successResponse.get("description").asText().isNotBlank()
                }
                assertTrue(hasSchemaOrExample, "Iteration $iteration: Success response for ${endpoint.method} ${endpoint.path} should have schema or example")

                // And: error response should have a description
                val errorResponseCode = responses.fieldNames().asSequence().first { it.startsWith("4") }
                val errorResponse = responses.get(errorResponseCode)
                assertNotNull(errorResponse, "Iteration $iteration: Error response should exist for ${endpoint.method} ${endpoint.path}")

                val errorDescription = errorResponse.get("description")
                assertNotNull(errorDescription, "Iteration $iteration: Error response for ${endpoint.method} ${endpoint.path} should have a description")
                assertTrue(errorDescription.asText().isNotBlank(), "Iteration $iteration: Error response description should not be blank for ${endpoint.method} ${endpoint.path}")
            }
        }
    }

