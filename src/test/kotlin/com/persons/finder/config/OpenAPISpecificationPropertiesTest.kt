@SpringBootTest
@AutoConfigureMockMvc
class OpenAPISpecificationPropertiesTest {

    @Autowired
    private lateinit var mockMvc: MockMvc

    @Autowired
    private lateinit var objectMapper: ObjectMapper
    
    // --- Helper Methods ---

    /**
     * Fetches and returns the OpenAPI specification.
     * Note: This is called for each property test iteration due to jqwik/Spring integration limitations.
     */
    private fun getOpenAPISpec(): JsonNode {
        val result = mockMvc.perform(get("/v3/api-docs"))
            .andExpect(status().isOk)
            .andReturn()
        return objectMapper.readTree(result.response.contentAsString)
    }

